				<h3 class="text-center mb-4">Background of the TRANS4M-PH Project</h3>
				<p>The "Transformative Competency-Based Public Health Education for Professional Employability in Bangladesh’s Health Sector” Project is a 3-year (2019-2022) Erasmus+ project funded by the EU. The aim of the project is to design and offer indigenous competency based public health curricula which sustain professional employability in Bangladesh’s health sector.</p>
				<p>The objective of the project is to upgrade priority public health courses to Competency-Based Public Health Curricula in 3 Bangladeshi Public Health Higher Education providers: BRAC James P Grant School of Public Health (BRAC JPGSPH); Asian University for Women (AUW); and International Centre for Climate Change and Development, Independent University, Bangladesh (ICCCAD-IUB). The Project’s European capacity-building technical partners are Maastricht University (UM), Netherlands and Institute of Development Studies (IDS), University of Sussex, United Kingdom.</p>

				<h3 class="text-center mb-4">Objectives of the TRANS4M-PH Project</h3>
				<p>Chronic high-skilled workforce shortages in Bangladesh’s Public Health Sector are a formidable constraint to the nation’s sustainable human development. Primarily stemming from their Public Health programmers, HEI are unable to meet the priority skill demands of the sector’s NGOs, government agencies, multilateral partners and other stakeholders. The academic learning model coupled with the faculty teaching capacities, cannot equip graduates for the real-world professional skill sets to constructively engage community Public Health needs.</p>
				<p>The Bangladesh Government has adopted an ‘Employment Growth-led Model’ for the country’s economic uplift, citing sector-based workforce development as its key driver for sustainable employment. This project also explicitly aligns with the Bangladesh Education Policy 2010; the Skills Development Policy 2011; which all identify Competency-based Education and Lifelong Learning; Job Skills Training, Learner-focused Teaching and pedagogy; Continual Faculty Development; and Professional employability and productivity as strategic priorities.</p>
				<p>Public Health Higher Education must align with these progressive national policies to comprehensively address its systemic deficiencies. It is therefore mission-critical to devise a multi-faceted solution to achieve the desired education outcomes for our valued students and future workforce. This model must be systematically redesigned around the student learner’s competency development by introducing the following four interdependent components in the academic learning endeavor:</p> 
				<ol>
					<li>Public Health Competency-based Curriculum</li>
					<li>Public Health Learning Methodologies</li>
					<li>Faculty Facilitator Development</li>
					<li>Faculty Professional Skills Training</li>
				</ol>
				<p>BRAC JPGSPH, ICCCAD-IUB and AUW recognize the economic imperative for Higher Education transformation to develop successive generations of professionals to positively impact the public health sector. These institutions have undertaken steps to improve student’s learning experience, with BRAC JPGSPH piloting competency-based courses and conducting Employer Demand Assessments; AUW’s Center for Teaching & Scholarship; and ICCCAD- IUB’s faculty initiatives. Thus validating their need for best-practice curriculum and the Competency-based learning imperative for Bangladesh.</p>
