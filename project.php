				<h4><img src="assets/images/project/project icons/Icon-1.png" alt="Project Icon Image">TO UPGRADE PRIORITY PUBLIC HEALTH (PH) COURSES TO COMPETENCY-BASED PH CURRICULA</h4>

				<p>These institutions have undertaken steps to improve student’s learning experience, with BRACSPH piloting competency-based courses and conducting Employer Demand Assessments; AUW’s Center for Teaching & Scholarship; and ICCCAD- IUB’s faculty initiatives. Thus validating their need for best-practice curriculum and the Competency-based learning imperative for Bangladesh. However,adopting this approach requires students to simulate and practice real-world workforce skills. This is achieved by centering its learning praxis on students and employers’ skill needs to ensure graduates’ public health workforce readiness.</p> 

				<h4><img src="assets/images/project/project icons/Icon-2.png" alt="Project Icon Image">TO DEVELOP CUTTING-EDGE, CONTEXTUALIZED PH LEARNING METHODOLOGIES FOR COMPETENCY-BASED CURRICULA</h4>

				<p>This Curriculum requires practical, real-world learning methodologies to develop the appropriate  competencies for respective public health fields. Learners must immerse themselves in community-based environments; to develop critical thinking, knowledge and understanding. This integral need for appropriate Public Health pedagogy requires formative steps of learning methodological design through a group of priority courses while concurrently developing local faculty capabilities.</p> 

				<h4><img src="assets/images/project/project icons/Icon-3.png" alt="Project Icon Image">TO DEVELOP REQUISITE FACULTY TEACHING CAPACITIES FOR THE PRIORITY COMPETENCY-BASED COURSES AND THEIR CORRESPONDING LEARNING METHODOLOGIES</h4>

				<p>The success of this Competency-based Curriculum system rests with the Faculty, who must conceptualize, manage and improvise their courses to ensure concrete competency outcomes. It is now self-evident that effective teachers are the critical factor for ensuring learning outcomes. However, Public Health Faculty are not held accountable or assessed accordingly. Given that current teaching is mismatched with real-world public health competency demands,It is critical that our Public Health Faculty cultivate their passion for teaching with the appropriate practical knowledge, field experience and creativity to train future public health professionals. Faculty capacity will be upskilled and grounded in Developing Country Public Health needs. Traditional faculty-centric teaching will thereby transition to learner-centric teaching, focusing on the learner’s priorities, real-world professional needs and skills development.</p> 

				<h4><img src="assets/images/project/project icons/Icon-4.png" alt="Project Icon Image">TO DEVELOP PROFESSIONAL SKILLS TRAINING MODULES AND AN E-LEARNING PLATFORM TO SUSTAIN FACULTY TEACHING & LEARNING CAPABILITIES</h4>

				<p>To initiate the process of indigenizing this extensive educational know-how and practice, the 3 partners will collate the Three Curriculum components’ design and practical learnings, into structured, formalized Faculty Professional Skills Development Training modules. Thus enabling knowledge and practice continuity, institutional memory and a self-generating cycle of Faculty capacity building. This will institutionalize the complete Competency-Based Curriculum system and enable the participating Faculty to become competent local resources for their entire Faculty to access the project’s Curriculum Design learning, practice and professional improvement. Given that there are no quality, international-standard Faculty training resources readily available in Bangladesh; these Competency-based Professional Trainings will pioneer a more rigorous self, peer, student and performance assessment system to develop teaching accountability and continual quality improvement. It is only when the core features of Competency-based Curriculum, Competency-based Learning Methodologies, Competency-based Faculty Development and Professional Competency Training, are integratively designed as a living educational ecosystem that we will more fully realize our students’ innate potential and contributions in the PH sector.</p>

